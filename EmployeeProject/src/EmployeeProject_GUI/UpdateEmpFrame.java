    private void txtEmpNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtEmpNoActionPerformed
        // TODO add your handling code here:
        String Eno=txtEmpNo.getText();
        try{
            int empNo=Integer.parseInt(Eno);
            Employee e=new Employee();
            e=EmployeeDAO.findEmployeeById(empNo);
            if(e==null)
            {
                JOptionPane.showMessageDialog(null, "Employee Not Found", "Wrong Empno", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            txtEmpNo.setText(String.valueOf(e.getEmpNo()));
            txtEmpName.setText(String.valueOf(e.getEmpName()));
            txtEmpSal.setText(String.valueOf(e.getEmpSalary()));
        }
        catch(NumberFormatException ex){
           JOptionPane.showMessageDialog(null, "Please Fill numeric data", "Error", JOptionPane.ERROR_MESSAGE);
           txtEmpNo.setText("");
           ex.printStackTrace();
           
       }
       catch(SQLException ex){
           JOptionPane.showMessageDialog(null, "DataBase Error", "DB Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
       }
    }//GEN-LAST:event_txtEmpNoActionPerformed

