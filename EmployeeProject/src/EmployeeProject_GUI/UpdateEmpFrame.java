    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        boolean b=validateInput();
        if(b==false){
            JOptionPane.showMessageDialog(null, "Please Fill all Details", "Incomplate Data", JOptionPane.ERROR_MESSAGE);
           return;
        }
        String eno=txtEmpNo.getText();
        String eName=txtEmpName.getText();
        String esal=txtEmpSal.getText();
        try{
            int empNo=Integer.parseInt(eno);
            double empSal=Double.parseDouble(esal);
            Employee e=new Employee(empNo,eName,empSal);
            boolean confirm=EmployeeDAO.UpdateEmp(e);
            if(confirm==false){
                JOptionPane.showMessageDialog(null, "Not update Please try again","Try again",JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(null, "Update SuccessFully","Success",JOptionPane.INFORMATION_MESSAGE);
                      
        }
        catch(NumberFormatException ex){
           JOptionPane.showMessageDialog(null, "Please Fill numeric data", "Error", JOptionPane.ERROR_MESSAGE);
           txtEmpNo.setText("");
           ex.printStackTrace();
           
       }
       catch(SQLException ex){
           JOptionPane.showMessageDialog(null, "DataBase Error", "DB Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
       }
    }//GEN-LAST:event_btnUpdateActionPerformed

